package com.github.treebbit.ch02;

public class FloatDoubleExample {
	public static void main(String[] args) {
		float f1 = 3.14F;
		double d1 = 3.14;
		
		System.out.println("f1: " + f1);
		System.out.println("d1: " + d1);
		
		float f2 = 3e6F;
		double d2 = 2e-3;
		
		System.out.println("f2: " + f2);
		System.out.println("d2: " + d2);
	}
}
